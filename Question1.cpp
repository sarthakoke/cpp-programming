#include <iostream> 
using namespace std; 
  
int main()  
{  
	int integersize;  
	char charactersize;  
	float floatsize;  
	double doublesize;  

	cout<< "Size of int is: "<<sizeof(integersize)<<"\n";  
	cout<< "Size of char is: "<<sizeof(charactersize)<<"\n";
	cout<< "Size of float is: "<<sizeof(floatsize)<<"\n"; 
	cout << "Size of double is: "<<sizeof(doublesize)<<"\n";  

	return 0;  
}
